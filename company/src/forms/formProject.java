    private void btnAddDeptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDeptActionPerformed
        // TODO add your handling code here:
        int autonum=Integer.parseInt(dept.getAutoNumber());
        String DeparmentName=(String) Tools.inputBox("Enter Department Name");
        String DeparmentLocation=(String) Tools.inputBox("Enter Department Location");
        dept.setDeptNo(autonum);
        dept.setDeptName(DeparmentName);
        dept.setLocation(DeparmentLocation);
        dept.add();
        try {
            dataBase.databaseConnector.fillComboBox("Department", "deptName", cbxDeptOfPro);
        } catch (SQLException ex) {
            Logger.getLogger(formProject.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnAddDeptActionPerformed


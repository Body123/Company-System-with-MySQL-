    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(Tools.confirmMessage("Do You Want To Delete")){
            setValues();
            proj.delete();
            clearData();
            proj.getAllRows(tblProject);
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed
    private void setValues(){
        int projNo=Integer.parseInt(txtProNo.getText());
        String name =txtProName.getText();
        String location =txtProLocation.getText();
        String deptName=cbxDeptOfPro.getSelectedItem().toString();
        int deptNo=Integer.parseInt(dept.getValueByName(deptName));
        proj.setDeptno(deptNo);
        proj.setProjectNAme(deptName);
        proj.setProjectno(projNo);
        proj.setProjectLocation(location);
        
    }

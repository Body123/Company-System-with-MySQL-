    private void tblProjectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProjectMouseClicked
        // TODO add your handling code here:
        try{
            int row =tblProject.getSelectedRow();
            txtProNo.setText((String) tblProject.getValueAt(row, 0));
            txtProName.setText((String) tblProject.getValueAt(row, 1));
            txtProLocation.setText((String) tblProject.getValueAt(row, 2));
            cbxDeptOfPro.setSelectedItem((String) tblProject.getValueAt(row, 4));
            btnAdd.setEnabled(false);
            btnUpdate.setEnabled(true);
            btnDelete.setEnabled(true);
        }catch(Exception e){
            Tools.msgBox(e.getMessage());
        }
    }//GEN-LAST:event_tblProjectMouseClicked

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
       String strSearch="Select * from project_data where ";
       if(rdoNo.isSelected()){
           strSearch+="PROJECT_NO ";
       }else if(rdoName.isSelected()){
           strSearch+="PROJECT_NAME ";
       }else if(rdoLocation.isSelected()){
           strSearch+="LOCATION ";
       }else{
           strSearch+="DEPT_NAME ";
       }
       strSearch+=" like'%"+txtSearch.getText()+"%'";
       proj.getCustomRow(strSearch, tblProject);
    }//GEN-LAST:event_btnSearchActionPerformed
